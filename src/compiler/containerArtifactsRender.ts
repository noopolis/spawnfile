const CONTAINER_ROOTFS_ROOT = "container/rootfs";
const GATEWAY_PORT_PLACEHOLDER = "<gateway-port>";
const WORKSPACE_PLACEHOLDER = "<workspace-path>";

const shellQuote = (value: string): string => `'${value.replace(/'/g, `'\"'\"'`)}'`;

const extractNodeMajorVersion = (image: string): number =>
  Number(image.match(/^node:(\d+)/)?.[1] ?? "0");

const createPackageInstallCommand = (packages: string[]): string =>
  `RUN apt-get update && apt-get install -y --no-install-recommends ${packages.join(" ")} && rm -rf /var/lib/apt/lists/*`;

const createNpmPackageInstallCommand = (packages: string[]): string =>
  `RUN npm install -g --omit=dev --no-fund --no-audit ${packages.join(" ")}`;

const selectBaseImage = (runtimePlans: RuntimeTargetPlan[]): string => {
  const firstRuntimeMeta = runtimePlans[0]?.meta;

  if (runtimePlans.length <= 1) {
    return firstRuntimeMeta?.standaloneBaseImage ?? "debian:bookworm-slim";
  }

  const nodeBaseImages = runtimePlans
    .map((plan) => plan.meta.standaloneBaseImage)
    .filter((image) => image.startsWith("node:"))
    .sort((left, right) => extractNodeMajorVersion(right) - extractNodeMajorVersion(left));

  return nodeBaseImages[0] ?? "debian:bookworm-slim";
};

export const renderEnvExample = (variables: ContainerEnvVariable[]): string => {
  if (variables.length === 0) {
    return "# No environment variables were detected during compile.\n";
  }

  const lines = ["# Generated by spawnfile compile", ""];
  const requiredVariables = variables.filter((variable) => variable.required);
  const optionalVariables = variables.filter((variable) => !variable.required);

  if (requiredVariables.length > 0) {
    lines.push("# Required");
    for (const variable of requiredVariables) {
      lines.push(`# ${variable.description}`);
      lines.push(`${variable.name}=`);
      lines.push("");
    }
  }

  if (optionalVariables.length > 0) {
    lines.push("# Optional");
    for (const variable of optionalVariables) {
      lines.push(`# ${variable.description}`);
      lines.push(`${variable.name}=`);
      lines.push("");
    }
  }

  return `${lines.join("\n").trimEnd()}\n`;
};

export const renderDockerfile = async (
  runtimePlans: RuntimeTargetPlan[],
  options: EntrypointOptions = {}
): Promise<string> => {
  const runtimeNames = [...new Set(runtimePlans.map((plan) => plan.runtimeName))];
  const runtimeRecipes = await Promise.all(
    runtimeNames.map((runtimeName) => createRuntimeInstallRecipe(runtimeName))
  );
  const baseImage = selectBaseImage(runtimePlans);
  const needsJsonEnvWriter = runtimePlans.some(
    (plan) => (plan.configEnvBindings?.length ?? 0) > 0
  );
  const systemDeps = [
    ...new Set([
      ...runtimePlans.flatMap((plan) => plan.meta.systemDeps),
      ...(needsJsonEnvWriter ? ["python3"] : [])
    ])
  ].sort();
  const globalNpmPackages = [
    ...new Set(runtimePlans.flatMap((plan) => plan.meta.globalNpmPackages ?? []))
  ].sort();
  const runtimePorts = runtimePlans.flatMap((plan) =>
    plan.publishedPort ? [plan.publishedPort] : []
  );
  const moltnetPorts = options.moltnetPublishedPorts ?? [];
  const exposedPorts = [...new Set([...runtimePorts, ...moltnetPorts])].sort(
    (left, right) => left - right
  );

  const lines = [];

  lines.push(`FROM ${baseImage}`);

  lines.push("USER root", "", "WORKDIR /opt/spawnfile");

  if (systemDeps.length > 0) {
    lines.push(createPackageInstallCommand(systemDeps), "");
  }

  if (globalNpmPackages.length > 0) {
    lines.push(createNpmPackageInstallCommand(globalNpmPackages), "");
  }

  for (const recipe of runtimeRecipes) {
    for (const copyCommand of recipe.copyCommands) {
      lines.push(copyCommand);
    }
    for (const command of recipe.commands) {
      lines.push(`RUN ${command}`);
    }
    lines.push("");
  }

  lines.push(
    'RUN if ! id -u spawnfile >/dev/null 2>&1; then useradd --create-home --home-dir /home/spawnfile --shell /bin/bash spawnfile; fi',
    ""
  );

  lines.push(
    "COPY container/rootfs/ /",
    "COPY .env.example /opt/spawnfile/.env.example",
    'COPY entrypoint.sh /opt/spawnfile/entrypoint.sh',
    "RUN chmod +x /opt/spawnfile/entrypoint.sh"
  );

  if (options.hasMoltnet) {
    lines.push(
      `COPY ${MOLTNET_BIN_DIRECTORY}/ /usr/local/bin/`,
      `RUN chmod +x ${MOLTNET_BINARY_NAMES.map((binaryName) => `/usr/local/bin/${binaryName}`).join(" ")}`
    );
  }

  if (options.hasTeamRouter) {
    lines.push(
      "COPY surface-router.js /opt/spawnfile/surface-router.js",
      "COPY router-config.json /opt/spawnfile/router-config.json"
    );
  }

  lines.push(
    "RUN mkdir -p /var/lib/spawnfile && chown -R spawnfile:spawnfile /var/lib/spawnfile /opt/spawnfile"
  );

  if (exposedPorts.length > 0) {
    lines.push(`EXPOSE ${exposedPorts.join(" ")}`);
  }

  lines.push("USER spawnfile");
  lines.push('ENTRYPOINT ["/opt/spawnfile/entrypoint.sh"]');
  return `${lines.join("\n").trimEnd()}\n`;
};

const createEnvironmentAssignments = (plan: RuntimeTargetPlan): string[] => {
  const envAssignments: string[] = [];

  if (plan.instancePaths.homePath) {
    envAssignments.push(`HOME=${shellQuote(plan.instancePaths.homePath)}`);
  }

  if (plan.meta.homeEnv && plan.instancePaths.homePath) {
    envAssignments.push(`${plan.meta.homeEnv}=${shellQuote(plan.instancePaths.homePath)}`);
  }

  if (plan.meta.configPathEnv) {
    envAssignments.push(`${plan.meta.configPathEnv}=${shellQuote(plan.instancePaths.configPath)}`);
  }

  if (plan.meta.portEnv && plan.port) {
    envAssignments.push(`${plan.meta.portEnv}=${shellQuote(String(plan.port))}`);
  }

  for (const [name, value] of Object.entries(plan.meta.staticEnv ?? {}).sort(([left], [right]) =>
    left.localeCompare(right)
  )) {
    envAssignments.push(`${name}=${shellQuote(value)}`);
  }

  return envAssignments;
};

const createEnvFileWrites = (plan: RuntimeTargetPlan): string[] =>
  plan.envFiles.map(
    (binding) => `write_env_file ${shellQuote(binding.envName)} ${shellQuote(binding.filePath)}`
  );

const createConfigEnvWrites = (plan: RuntimeTargetPlan): string[] =>
  (plan.configEnvBindings ?? []).map(
    (binding) =>
      `apply_json_env_value ${shellQuote(plan.instancePaths.configPath)} ${shellQuote(binding.envName)} ${shellQuote(binding.jsonPath)}`
  );

const resolveStartCommand = (plan: RuntimeTargetPlan): string[] =>
  plan.meta.startCommand
    .map((token) =>
      token
        .replaceAll("<runtime-root>", plan.runtimeRoot)
        .replaceAll("<port>", plan.port ? String(plan.port) : "")
    )
    .filter((token) => token.length > 0);

const createRuntimeReadinessWait = (plan: RuntimeTargetPlan): string[] => {
  if (plan.runtimeName !== "openclaw" || !plan.port) {
    return [];
  }

  return [
    "attempts=0",
    `until curl -sf ${shellQuote(`http://127.0.0.1:${plan.port}/healthz`)} >/dev/null; do`,
    "  attempts=$((attempts + 1))",
    '  if [ "$attempts" -ge 180 ]; then',
    `    echo ${shellQuote(`Timed out waiting for ${plan.runtimeName} on port ${plan.port}`)} >&2`,
    "    exit 1",
    "  fi",
    "  sleep 1",
    "done",
    ""
  ];
};

export const createRootfsFiles = (runtimePlans: RuntimeTargetPlan[]): EmittedFile[] =>
  runtimePlans.flatMap((plan) =>
    plan.targetFiles.map((file) => {
      if (file.path === plan.meta.configFileName) {
        return {
          content: file.content
            .replaceAll(WORKSPACE_PLACEHOLDER, plan.instancePaths.workspacePath)
            .replaceAll(
              `"${GATEWAY_PORT_PLACEHOLDER}"`,
              plan.port ? String(plan.port) : "0"
            )
            .replaceAll(GATEWAY_PORT_PLACEHOLDER, plan.port ? String(plan.port) : ""),
          path: `${CONTAINER_ROOTFS_ROOT}${plan.instancePaths.configPath}`
        };
      }

      if (file.path.startsWith("workspace/")) {
        const relativeWorkspacePath = file.path.slice("workspace/".length);
        return {
          content: file.content,
          path: `${CONTAINER_ROOTFS_ROOT}${path.posix.join(
            plan.instancePaths.workspacePath,
            relativeWorkspacePath
          )}`
        };
      }

      if (file.path.startsWith("home/")) {
        if (!plan.instancePaths.homePath) {
          throw new SpawnfileError(
            "runtime_error",
            `Container target ${plan.id} for ${plan.runtimeName} emitted home-scoped files without a home path`
          );
        }

        const relativeHomePath = file.path.slice("home/".length);
        return {
          content: file.content,
          path: `${CONTAINER_ROOTFS_ROOT}${path.posix.join(
            plan.instancePaths.homePath,
            relativeHomePath
          )}`
        };
      }

      throw new SpawnfileError(
        "runtime_error",
        `Container target ${plan.id} for ${plan.runtimeName} emitted unsupported path ${file.path}`
      );
    })
  );

export interface EntrypointOptions {
  hasMoltnet?: boolean;
  hasTeamRouter?: boolean;
  moltnet?: {
    bridgePlans: MoltnetArtifacts["bridgePlans"];
    serverPlans: MoltnetArtifacts["serverPlans"];
  };
  moltnetPublishedPorts?: number[];
}

export const renderEntrypoint = (
  runtimePlans: RuntimeTargetPlan[],
  requiredSecrets: string[],
  options: EntrypointOptions = {}
): string => {
  const lines = [
    "#!/usr/bin/env bash",
    "set -euo pipefail",
    "",
    "require_env() {",
    '  local name=\"$1\"',
    '  if [ -z \"${!name:-}\" ]; then',
    '    echo \"Missing required env: $name\" >&2',
    "    exit 1",
    "  fi",
    "}",
    "",
    "require_file() {",
    '  local target=\"$1\"',
    '  if [ ! -f \"$target\" ]; then',
    '    echo \"Missing required file: $target\" >&2',
    "    exit 1",
    "  fi",
    "}",
    "",
    "write_env_file() {",
    '  local name=\"$1\"',
    '  local target=\"$2\"',
    '  if [ -z \"${!name:-}\" ]; then',
    "    return",
    "  fi",
    '  mkdir -p \"$(dirname \"$target\")\"',
    '  printf %s \"${!name:-}\" > \"$target\"',
    "}",
    "",
    "apply_json_env_value() {",
    '  local target=\"$1\"',
    '  local name=\"$2\"',
    '  local json_path=\"$3\"',
    '  if [ -z \"${!name:-}\" ]; then',
    "    return",
    "  fi",
    "  python3 - \"$target\" \"$name\" \"$json_path\" <<'PY'",
    "import json",
    "import os",
    "import sys",
    "",
    "target_path = sys.argv[1]",
    "env_name = sys.argv[2]",
    "json_path = sys.argv[3].split('.')",
    "value = os.environ.get(env_name)",
    "if value is None:",
    "    raise SystemExit(0)",
    "",
    "with open(target_path, encoding='utf-8') as handle:",
    "    data = json.load(handle)",
    "",
    "cursor = data",
    "for part in json_path[:-1]:",
    "    child = cursor.get(part)",
    "    if not isinstance(child, dict):",
    "        child = {}",
    "        cursor[part] = child",
    "    cursor = child",
    "",
    "cursor[json_path[-1]] = value",
    "",
    "with open(target_path, 'w', encoding='utf-8') as handle:",
    "    json.dump(data, handle, indent=2)",
    "    handle.write('\\n')",
    "PY",
    "}",
    ""
  ];

  for (const secretName of requiredSecrets) {
    lines.push(`require_env ${shellQuote(secretName)}`);
  }

  if (requiredSecrets.length > 0) {
    lines.push("");
  }

  lines.push(
    'if [ -n "${OPENCLAW_GATEWAY_TOKEN:-}" ] && [ -z "${OPENCLAW_HOOKS_TOKEN:-}" ]; then',
    '  export OPENCLAW_HOOKS_TOKEN="hooks-${OPENCLAW_GATEWAY_TOKEN}"',
    "fi",
    ""
  );

  const moltnetServerPlans = options.moltnet?.serverPlans ?? [];
  const moltnetBridgePlans = options.moltnet?.bridgePlans ?? [];

  if (runtimePlans.length === 1) {
    const plan = runtimePlans[0]!;
    const commandTokens = resolveStartCommand(plan);
    const envAssignments = createEnvironmentAssignments(plan);
    const envFileWrites = createEnvFileWrites(plan);
    const configEnvWrites = createConfigEnvWrites(plan);

    if (options.hasTeamRouter) {
      // Multi-process mode for single-runtime teams: router + runtime
      lines.push(
        "PIDS=()",
        "",
        "terminate_children() {",
        '  for pid in "${PIDS[@]:-}"; do',
        '    kill "$pid" 2>/dev/null || true',
        "  done",
        "}",
        "",
        "trap terminate_children INT TERM EXIT",
        "",
        ...(options.hasTeamRouter
          ? [
              "node /opt/spawnfile/surface-router.js /opt/spawnfile/router-config.json &",
              'PIDS+=("$!")',
              ""
            ]
          : []),
        ...moltnetServerPlans.flatMap((serverPlan) => [
          `MOLTNET_LISTEN_ADDR=${shellQuote(`:${serverPlan.port}`)} MOLTNET_NETWORK_ID=${shellQuote(serverPlan.networkId)} MOLTNET_NETWORK_NAME=${shellQuote(serverPlan.name)} /usr/local/bin/moltnet &`,
          'PIDS+=("$!")',
          ""
        ]),
        `mkdir -p ${shellQuote(plan.instancePaths.workspacePath)}`,
        `require_file ${shellQuote(plan.instancePaths.configPath)}`,
        ...envFileWrites,
        ...configEnvWrites,
        `${envAssignments.join(" ")} ${commandTokens.map(shellQuote).join(" ")} &`,
        'PIDS+=("$!")',
        "",
        ...createRuntimeReadinessWait(plan),
        ...moltnetServerPlans.flatMap((serverPlan) => [
          `until curl -sf ${shellQuote(`http://127.0.0.1:${serverPlan.port}/healthz`)} >/dev/null; do sleep 1; done`,
          ...serverPlan.rooms.map(
            (room) =>
              `curl -sf -X POST -H 'Content-Type: application/json' -d ${shellQuote(
                JSON.stringify({
                  id: room.id,
                  members: room.members
                })
              )} ${shellQuote(`http://127.0.0.1:${serverPlan.port}/v1/rooms`)} >/dev/null || true`
          ),
          ""
        ]),
        ...moltnetBridgePlans.flatMap((bridgePlan) => [
          `/usr/local/bin/moltnet-bridge ${shellQuote(bridgePlan.configPath)} &`,
          'PIDS+=("$!")',
          ""
        ]),
        "status=0",
        'for pid in "${PIDS[@]}"; do',
        '  if ! wait "$pid"; then',
        "    status=1",
        "  fi",
        "done",
        "",
        'exit "$status"'
      );
    } else {
      lines.push(
        `mkdir -p ${shellQuote(plan.instancePaths.workspacePath)}`,
        `require_file ${shellQuote(plan.instancePaths.configPath)}`,
        ...envFileWrites,
        ...configEnvWrites,
        `${envAssignments.join(" ")} exec ${commandTokens.map(shellQuote).join(" ")}`
      );
    }

    return `${lines.join("\n").trimEnd()}\n`;
  }

  lines.push(
    "PIDS=()",
    "",
    "terminate_children() {",
    '  for pid in "${PIDS[@]:-}"; do',
    '    kill "$pid" 2>/dev/null || true',
    "  done",
    "}",
    "",
    "trap terminate_children INT TERM EXIT",
    ""
  );

  if (options.hasTeamRouter) {
    lines.push(
      "node /opt/spawnfile/surface-router.js /opt/spawnfile/router-config.json &",
      'PIDS+=("$!")',
      ""
    );
  }

  for (const serverPlan of moltnetServerPlans) {
    lines.push(
      `MOLTNET_LISTEN_ADDR=${shellQuote(`:${serverPlan.port}`)} MOLTNET_NETWORK_ID=${shellQuote(serverPlan.networkId)} MOLTNET_NETWORK_NAME=${shellQuote(serverPlan.name)} /usr/local/bin/moltnet &`,
      'PIDS+=("$!")',
      ""
    );
  }

  for (const plan of runtimePlans) {
    const commandTokens = resolveStartCommand(plan);
    const envAssignments = createEnvironmentAssignments(plan);
    const envFileWrites = createEnvFileWrites(plan);
    const configEnvWrites = createConfigEnvWrites(plan);

    lines.push(
      `mkdir -p ${shellQuote(plan.instancePaths.workspacePath)}`,
      `require_file ${shellQuote(plan.instancePaths.configPath)}`,
      ...envFileWrites,
      ...configEnvWrites,
      `${envAssignments.join(" ")} ${commandTokens.map(shellQuote).join(" ")} &`,
      'PIDS+=("$!")',
      "",
      ...createRuntimeReadinessWait(plan)
    );
  }

  for (const serverPlan of moltnetServerPlans) {
    lines.push(
      `until curl -sf ${shellQuote(`http://127.0.0.1:${serverPlan.port}/healthz`)} >/dev/null; do sleep 1; done`
    );

    for (const room of serverPlan.rooms) {
      lines.push(
        `curl -sf -X POST -H 'Content-Type: application/json' -d ${shellQuote(
          JSON.stringify({
            id: room.id,
            members: room.members
          })
        )} ${shellQuote(`http://127.0.0.1:${serverPlan.port}/v1/rooms`)} >/dev/null || true`
      );
    }

    lines.push("");
  }

  for (const bridgePlan of moltnetBridgePlans) {
    lines.push(
      `/usr/local/bin/moltnet-bridge ${shellQuote(bridgePlan.configPath)} &`,
      'PIDS+=("$!")',
      ""
    );
  }

  lines.push(
    'if [ "${#PIDS[@]}" -eq 0 ]; then',
    '  echo "No runtime targets were generated for this compile output" >&2',
    "  exit 1",
    "fi",
    "",
    "status=0",
    'for pid in "${PIDS[@]}"; do',
    '  if ! wait "$pid"; then',
    "    status=1",
    "  fi",
    "done",
    "",
    'exit "$status"'
  );

  return `${lines.join("\n").trimEnd()}\n`;
};
